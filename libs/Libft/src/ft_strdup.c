#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dup;
	size_t	i;

	if (!str)
		return (NULL);
	dup = (char *)malloc((ft_strlen(str) + 1) * sizeof(char));
	i = 0;
	if (!dup)
		return (NULL);
	while (str[i])
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
